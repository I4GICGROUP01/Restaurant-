    <p class="writer">While the Kingdom may have drawn on influences from its neighbours, Thailand and Vietnam, as well as India and China thousands of years ago, Cambodian food has its own distinct flavour.
        This ancient cuisine, handed down from mother to daughter, is famous for its delicate blend of herbs and spices. Cambodian dishes strike a perfect balance of salty, sweet, sour and bitter flavours without being overtly hot and spicy.
        Due to the expansive waterways that carve their way through the countryside, in particular the Mekong, fish has always been a key ingredient as well as rice. In fact the main trademark of Khmer cuisine is prahok, a fermented fish paste, known for its pungent smell and unique taste.</p>
